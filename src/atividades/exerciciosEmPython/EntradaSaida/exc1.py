#Faça um algoritmo para ler três números inteiros e escrever a média dos números lidos

print("Entre com três números inteiros para cacular sua média!!")

n1 = int(input("Entre com o primeiro número: "))
n2 = int(input("Entre com o segundo número: "))
n3 = int(input("Entre com o terceiro número: "))
media = (n1 + n2 + n3) / 3

print("A media dos números inseridos é ", media)
