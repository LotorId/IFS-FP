#Construa um programa que leia quatro valores inteiros A, B, C e D. A seguir, calcule
#e mostre a diferença do produto de A e B pelo produto de C e D.

a = int(input("Entre com um valor para A: "))
b = int(input("Entre com um valor para B: "))
c = int(input("Entre com um valor para C "))
d = int(input("Entre com um valor para D: "))

diferenca = (a * b) - (c * d)

print("A diferença do produto de A e B pelo produto de C e D é: ", diferenca)
