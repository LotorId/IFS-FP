#Faça um algoritmo para ler dois números inteiros e informar se estes números são iguais ou diferentes.

n1 = int(input("Entre com um número: "))
n2 = int(input("Entre com outro número: "))

if n1 == n2:
    print("Os números", n1, "e", n2, "são iguais")
else:
    print("Os números", n1, "e", n2, "são diferentes")